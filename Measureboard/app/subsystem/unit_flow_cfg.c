static uint32_t GetDuringTime_SubStep_Ms(StepConfig step)
{
	uint32_t time = 0;
	uint32_t temp = 0;
	if((step.pump1.pps) &&(step.pump1.steps))
	{
		temp = (uint32_t)(((uint64_t)(step.pump1.steps*1000)/step.pump1.pps));
		if(temp >time)		time = temp;
	}
	if((step.pump2.pps) &&(step.pump2.steps))
	{
		temp = (uint32_t)(((uint64_t)(step.pump2.steps*1000)/step.pump2.pps));
		if(temp >time)		time = temp;
	}
	if((step.pump3.pps) &&(step.pump3.steps))
	{
		temp = (uint32_t)(((uint64_t)(step.pump3.steps*1000)/step.pump3.pps));
		if(temp >time)		time = temp;
	}
	return time + VALVE_DURING_TIME;
}

static uint32_t GetDuringTime_MainStep_Ms(uint16_t subStep)
{
	uint32_t timeMs = 0;
	for(uint16_t idx = 0; idx<MAX_MAIN_SUBSTEPS; idx++)
	{
		uint8_t step = stepsConfig[subStep-A_0][idx];
		if(step == 0)
			break;
		else
			timeMs += GetDuringTime_Step_Ms(step);
	}

	if(timeMs > 0)
	{
	//	timeMs += TSK_SW_TIME_N;
	}
	else
		timeMs = 100;
	return timeMs;
}

static uint32_t GetDuringTime_Step_Ms(uint16_t subStep)
{
	if(IN_RANGE(subStep, STEP_0, STEP_MAX))
	{
		return GetDuringTime_SubStep_Ms(allStepsConfig[subStep-STEP_0]);
	}
	else if(IN_RANGE(subStep, SPECIAL_IDX_0, SPECIAL_IDX_MAX))
	{
		return GetDuringTime_SP(subStep);
	}
	else if(IN_RANGE(subStep, TIME_0, TIME_MAX))
	{
		return timeConfig[subStep-TIME_0]*TIME_UNIT;
	}
	else if(IN_RANGE(subStep, A_0, A_MAX))
	{
		return GetDuringTime_MainStep_Ms(subStep);
	}
	else
	{}
	return 0;
}

uint32_t CalcDuringTimeMsStep_WithDelay(uint16_t subStep)
{
	uint32_t val = GetDuringTime_Step_Ms(subStep);
	return val + TSK_SW_TIME_NEW;
}





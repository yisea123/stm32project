const char* GetStepName(uint16_t chn)
{
	static const char defaultStepName[] = "UNKNOWN_NAME";
	static const char nullStepName[] = "\0\0";
	const char* adr = NULL;

	if(chn == 0)
		adr = nullStepName;
	else if(chn < OBJ_IDX_MAX)
		adr = StepName[chn-1].stepName;

	if(adr == NULL)
		adr = &defaultStepName[0];
	return adr;
}


void FakeParaInit(uint16_t val)
{
	if(val)
	{
		for(uint16_t id = 0; id <MAX_SUBSTEP_PUMP; id++)
		{
			if((allStepsConfig[id].pump1.pps != 0) && (allStepsConfig[id].pump1.steps != 0))
			{
				allStepsConfig[id].pump1.steps = (uint32_t)(allStepsConfig[id].pump1.pps * val);
			}
			if((allStepsConfig[id].pump2.pps != 0) && (allStepsConfig[id].pump2.steps != 0))
			{
				allStepsConfig[id].pump2.steps = (uint32_t)(allStepsConfig[id].pump2.pps * val);
			}
			if((allStepsConfig[id].pump3.pps != 0) && (allStepsConfig[id].pump3.steps != 0))
			{
				allStepsConfig[id].pump3.steps = (uint32_t)(allStepsConfig[id].pump3.pps * val);
			}
		}
	}
	else
	{
		memcpy((void*)&allStepsConfig, (void*)&allSteps_Default, sizeof(allSteps_Default));
	}
	Trigger_EEPSave((void*)&allStepsConfig, sizeof(allStepsConfig), SYNC_CYCLE);
}



